def find_differences(text1, text2):
    # 使用中文句号和换行符分割文本
    sentences1 = [s for s in text1.replace('。', '\n').split('\n') if s]
    sentences2 = [s for s in text2.replace('。', '\n').split('\n') if s]
    
    # 找出不同的句子
    diff = []
    for s1 in sentences1:
        if s1 not in sentences2:
            for s2 in sentences2:
                if s2 not in sentences1:
                    diff.append([s1, s2])
                    sentences2.remove(s2)  # 移除已经找到差异的句子，避免重复
                    break
    
    return diff

